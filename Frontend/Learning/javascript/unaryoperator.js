// pre-increment/decrement 
let a=10;

let newage=++a;
console.log(a);
console.log(newage);

let b=20;
let newb=--b;
console.log(b);
console.log(newb);

//post-increment/decrement 

let a2=10;


let newa2=a2++;
console.log(a2);
console.log(newa2);

let b2=10;

let newb2=b2--;
console.log(b2);
console.log(newb2);

